function createCircles1() {
  var button = document.getElementById("lotus-btn1");
  // Create small div elements
  for (var i = 0; i < 20; i++) {
    var circle = document.createElement("div");
    circle.classList.add("circle");
    document.getElementById("circle-container1").appendChild(circle);
    var x = Math.floor(Math.random() * 600) - 300;
    var y = Math.floor(Math.random() * 600) - 300;
    circle.style.setProperty('--x', x + "px");
    circle.style.setProperty('--y', y + "px");
  }
}

